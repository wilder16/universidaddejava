package secciones.secc3tiposprimitivos;

public class Temario2 {

	public static void main(String[] args) {
	
		// Primitivos tipo flotante 
	
		// float, double
		
		float numeroFloat = (float) 10.5;
		System.out.println(numeroFloat);
		System.out.println("Valor minimo de float: " + Float.MIN_VALUE);
		System.out.println("Valor maximo del float: " + Float.MAX_VALUE);
		
		numeroFloat = (float) 3.4028235E39D;
		System.out.println(numeroFloat);
		
		double numeroDouble =  10.5;
		System.out.println(numeroDouble);
		System.out.println("Valor minimo de float: " + Double.MIN_VALUE);
		System.out.println("Valor maximo del float: " + Double.MAX_VALUE);

	}

}
